import { Request, Response } from 'express';
import { BusService } from '../services/busService';

const busService = new BusService();

//============================================================================================================================================================



export class BusController{

    // =================================================================================================================================
    //? Add Bus
    // =================================================================================================================================

    async addBus(req:Request, res:Response){
        return busService.addBus(req, res);
    }

    // =================================================================================================================================
    //? Remove
    // =================================================================================================================================
    async removeBus(req:Request, res:Response){
        return busService.removeBus(req, res);
    }

    // =================================================================================================================================
    //? Update
    // =================================================================================================================================
    async updateBus(req:Request, res:Response){
        return busService.updateBus(req, res);
    }

}