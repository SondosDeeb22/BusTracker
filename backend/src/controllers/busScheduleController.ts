import { Request, Response } from 'express';
import { BusScheduleService } from '../services/busScheduleService';

const busScheduleService = new BusScheduleService();

//============================================================================================================================================================

export class BusScheduleController {

    // =================================================================================================================================
    // Add Schedule
    async addSchedule(req: Request, res: Response) {
        return busScheduleService.addScheduleRecord(req, res);
    }

    // =================================================================================================================================
    // Remove Schedule
    async removeSchedule(req: Request, res: Response) {
        return busScheduleService.removeSchedulRecord(req, res);
    }

    // =================================================================================================================================
    // Update Schedule
    // =================================================================================================================================
    async updateSchedule(req: Request, res: Response) {
        return busScheduleService.updateScheduleRecord(req, res);
    }

    // =================================================================================================================================
    // Fetch All Schedules
    // =================================================================================================================================
    async getSchedules(req: Request, res: Response) {
        return busScheduleService.getBusSchedule(req, res);
    }
}